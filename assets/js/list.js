// var shuffledlist = shuffle(list);
function shuffle(sourceArray) {
    for (var i = 0; i < 11; i++) {
        var j = i + Math.floor(Math.random() * (sourceArray.length - i));

        var temp = sourceArray[j];
        sourceArray[j] = sourceArray[i];
        sourceArray[i] = temp;
    }
    return sourceArray;
}

